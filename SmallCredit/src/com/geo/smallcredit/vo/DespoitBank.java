package com.geo.smallcredit.vo;

public class DespoitBank {

	private String status;
	private String desc;
	private String bankcard_binding_id;
	private String bankcardNumber;

	public DespoitBank(String status, String desc, String bankcard_binding_id,
			String bankcardNumber) {
		super();
		this.status = status;
		this.desc = desc;
		this.bankcard_binding_id = bankcard_binding_id;
		this.bankcardNumber = bankcardNumber;
	}

	public DespoitBank() {
		super();
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getBankcard_binding_id() {
		return bankcard_binding_id;
	}

	public void setBankcard_binding_id(String bankcard_binding_id) {
		this.bankcard_binding_id = bankcard_binding_id;
	}

	public String getBankcardNumber() {
		return bankcardNumber;
	}

	public void setBankcardNumber(String bankcardNumber) {
		this.bankcardNumber = bankcardNumber;
	}

}
