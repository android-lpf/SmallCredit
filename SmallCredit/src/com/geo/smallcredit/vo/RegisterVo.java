package com.geo.smallcredit.vo;

public class RegisterVo {

	private int code;
	private String userid;
	private String phone;
	private String info;
	private String token;
	
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public RegisterVo(int code, String userid, String phone, String info,String token) {
		super();
		this.code = code;
		this.userid = userid;
		this.phone = phone;
		this.info = info;
		this.token=token;
	}
	public RegisterVo() {
		super();
	}
	
}
